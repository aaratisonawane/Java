package com.company;

public class IntDoubleDemo {

    public static void main(String[] args) {

        int x = 10;
        double y = 10.0;


        System.out.println("Original x: " + x);
        System.out.println("Original y: " + y);

        System.out.println(x/4);
        System.out.println(y/4);


    }
}
